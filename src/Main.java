    public void score(){
        int sum = 0;
        for (int[] ints : board) {
            for (int anInt : ints) {
                sum += anInt;
            }
        }
        System.out.println("\n\n" + "Your score is: " + sum);
    }

    public void add() {
        System.out.println("press   'w' / 'a' / 's' / 'd'   to move the numbers");
        Scanner scanner = new Scanner(System.in);
        String response = scanner.nextLine().toLowerCase();

        //Approach:
        // 1. Move the fields in the specified direction.
        // 2. Combine adjacent fields with equal values.
        // 3. Move again to close any gaps that have formed.

        switch (response) {
            case "a" -> {
                // Code to move left
                for (int r = 0; r < board.length; r++) {
                    int writePos = 0;
                    // move
                    for (int c = 0; c < board.length; c++) {
                        if (board[r][c] != 0) {
                            board[r][writePos] = board[r][c];
                            if (writePos != c) {
                                board[r][c] = 0;
                            }
                            writePos++;
                        }
                    }
                    // add
                    for (int c = 0; c < board.length - 1; c++) {
                        if (board[r][c] == board[r][c + 1] && board[r][c] != 0) {
                            board[r][c] *= 2;
                            board[r][c + 1] = 0;
                            c++;
                        }
                    }
                    // move again to close gaps
                    writePos = 0;
                    for (int c = 0; c < board.length; c++) {
                        if (board[r][c] != 0) {
                            board[r][writePos] = board[r][c];
                            if (writePos != c) {
                                board[r][c] = 0;
                            }
                            writePos++;
                        }
                    }
                }
            }
            case "d" -> {
                // Code to move right
                for (int r = 0; r < board.length; r++) {
                    int writePos = board.length - 1;
                    for (int c = board.length - 1; c >= 0; c--) {
                        if (board[r][c] != 0) {
                            board[r][writePos] = board[r][c];
                            if (writePos != c) {
                                board[r][c] = 0;
                            }
                            writePos--;
                        }
                    }
                    for (int c = board.length - 1; c > 0; c--) {
                        if (board[r][c] == board[r][c - 1] && board[r][c] != 0) {
                            board[r][c] *= 2;
                            board[r][c - 1] = 0;
                            c--;
                        }
                    }
                    writePos = board.length - 1;
                    for (int c = board.length - 1; c >= 0; c--) {
                        if (board[r][c] != 0) {
                            board[r][writePos] = board[r][c];
                            if (writePos != c) {
                                board[r][c] = 0;
                            }
                            writePos--;
                        }
                    }
                }
            }
            case "w" -> {
                // Code to move up
                for (int c = 0; c < board.length; c++) {
                    int writePos = 0;
                    for (int r = 0; r < board.length; r++) {
                        if (board[r][c] != 0) {
                            board[writePos][c] = board[r][c];
                            if (writePos != r) {
                                board[r][c] = 0;
                            }
                            writePos++;
                        }
                    }
                    for (int r = 0; r < board.length - 1; r++) {
                        if (board[r][c] == board[r + 1][c] && board[r][c] != 0) {
                            board[r][c] *= 2;
                            board[r + 1][c] = 0;
                            r++;
                        }
                    }
                    writePos = 0;
                    for (int r = 0; r < board.length; r++) {
                        if (board[r][c] != 0) {
                            board[writePos][c] = board[r][c];
                            if (writePos != r) {
                                board[r][c] = 0;
                            }
                            writePos++;
                        }
                    }
                }
            }
            case "s" -> {
                // Code to move down
                for (int c = 0; c < board.length; c++) {
                    int writePos = board.length - 1;
                    for (int r = board.length - 1; r >= 0; r--) {
                        if (board[r][c] != 0) {
                            board[writePos][c] = board[r][c];
                            if (writePos != r) {
                                board[r][c] = 0;
                            }
                            writePos--;
                        }
                    }
                    for (int r = board.length - 1; r > 0; r--) {
                        if (board[r][c] == board[r - 1][c] && board[r][c] != 0) {
                            board[r][c] *= 2;
                            board[r - 1][c] = 0;
                            r--;
                        }
                    }
                    writePos = board.length - 1;
                    for (int r = board.length - 1; r >= 0; r--) {
                        if (board[r][c] != 0) {
                            board[writePos][c] = board[r][c];
                            if (writePos != r) {
                                board[r][c] = 0;
                            }
                            writePos--;
                        }
                    }
                }
            }
        }
    }

